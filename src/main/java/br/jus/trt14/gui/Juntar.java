    private void jBJuntarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBJuntarActionPerformed
        if (jTblListaPDF.getRowCount() > 1) {
            if (!jTFSaidaPath.getText().isEmpty()) {
                boolean erro = false;
                int rowCount = jTblListaPDF.getRowCount();
                List<String> listaArquivos = new ArrayList<String>();
                for (int i = 0; i < rowCount; i++) {
                    String fileName = (String) jTblListaPDF.getValueAt(i, 0);
                    File file = new File(Utils.getRandomFileName());
                    Archive.copy(new File(fileName), file);
                    listaArquivos.add(file.getAbsolutePath());
                    if (fileName.equals(jTFSaidaPath.getText())) {
                        erro = true;
                        break;
                    }
                }

                if (erro) {
                    JOptionPane.showMessageDialog(null, "Por favor, o arquivo de saída não pode ser o mesmo de origem!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    TelaConvertendo telaConvertendo = new TelaConvertendo();
                    telaConvertendo.iniciarDialog(listaArquivos, jTFSaidaPath.getText(), this);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Por favor, selecione o local onde deseja salvar o arquivo convertido", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione pelo menos 2 arquivos", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBJuntarActionPerformed

