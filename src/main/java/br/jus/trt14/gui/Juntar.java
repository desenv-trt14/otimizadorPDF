    void iniciarArgumentos(String saida, List<String> listaArquivos) {
        jTFSaidaPath.setText(saida);
        for (String arq : listaArquivos) {
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel = (DefaultTableModel) jTblListaPDF.getModel();
            String pageCount = String.valueOf(Utils.getPageCount(arq));
            if (!pageCount.equals("0")) {
                tableModel.addRow(new Object[]{arq, pageCount});
            }
        }
        this.setVisible(true);

    }

