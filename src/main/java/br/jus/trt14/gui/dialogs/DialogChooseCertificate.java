    private void constructTableCertificate(List<Object[]> certificates) {
        if (certificates.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não consegui obter a lista de certificados!\nPor favor, verifique se o seu certificado está inserido corretamente!\n"
                    + "Caso ainda esteja tendo problemas experimente acessar o serviço e-CAC da Receita Federal");
            try {
                URL url = new URL("https://cav.receita.fazenda.gov.br/eCAC/publico/login.aspx");
                Utils.openWebpage(url.toURI());
            } catch (Exception ex) {
                Logger.getLogger(Atualizacao.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
            return;
        }
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTCertificado.getModel();
        for (Object[] obj : certificates) {
            Object[] extractCertificateInfo = Utils.extractCertificateInfo(obj);
            defaultTableModel.addRow(extractCertificateInfo);
        }
        jTCertificado.setRowSelectionInterval(0, 0);
        this.setVisible(true);
        this.setModal(true);
        Utils.detectEnterTableAction(jTCertificado, jBEscolherCertificado);

    }

