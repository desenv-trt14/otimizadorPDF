    private void jBEscolherCertificadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEscolherCertificadoActionPerformed

        DefaultTableModel defaultTableModel = (DefaultTableModel) jTCertificado.getModel();
        if (defaultTableModel.getRowCount() > 0) {
            int selectedRow = jTCertificado.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Você precisa escolher um certificado");
                return;
            }
            selectedAlias = defaultTableModel.getValueAt(selectedRow, 0).toString();
        } else {
            JOptionPane.showMessageDialog(null, "Não consegui obter a lista de certificados");
        }
        DialogChooseCertificate.this.dispatchEvent(new WindowEvent(
                DialogChooseCertificate.this, WindowEvent.WINDOW_CLOSING));


    }//GEN-LAST:event_jBEscolherCertificadoActionPerformed

